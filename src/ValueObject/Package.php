<?php
namespace FsDeliverySdk\ValueObject;

class Package implements RequestParamsInterface
{
    use TraitFilter;

    /** @var float */
    private $length; // Длинна в см
    /** @var float */
    private $height; // Высота в см
    /** @var float */
    private $width; // Ширина в см
    /** @var float */
    private $weight; // Вес в кг

    /**
     * @return float
     */
    public function getLength()
    {
        return $this->length;
    }

    /**
     * @param float $length
     */
    public function setLength($length)
    {
        $this->length = $length;
        return $this;
    }

    /**
     * @return float
     */
    public function getHeight()
    {
        return $this->height;
    }

    /**
     * @param float $height
     */
    public function setHeight($height)
    {
        $this->height = $height;
        return $this;
    }

    /**
     * @return float
     */
    public function getWidth()
    {
        return $this->width;
    }

    /**
     * @param float $width
     */
    public function setWidth($width)
    {
        $this->width = $width;
        return $this;
    }

    /**
     * @return float
     */
    public function getWeight()
    {
        return $this->weight;
    }

    /**
     * @param float $weight
     */
    public function setWeight($weight)
    {
        $this->weight = $weight;
        return $this;
    }
}